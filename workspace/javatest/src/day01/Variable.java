package day01;

public class Variable {

  public static void main(String[] args) {
    boolean sw = true;
    System.out.println("sw:" + sw);
    char grade = '수';
    System.out.println("grade:" + grade);
    String name = "소프트웨어";
    System.out.println("name:" + name);
    int year = 2021;
    System.out.println("year:" + year);
    double ija = 0.04;
    System.out.println("ija:" + ija);

  }

}
